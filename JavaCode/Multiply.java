public class Multiply {
    public static int multiply(int a, int b){
        int c = a * b;
        return c;
    }

    public static void main(Strings[] args) {
        int prod = multiply(5, 10);
        System.out.println("Product: " + prod);
        prod = multiply(7, 3);
        System.out.println("Product: " + prod);
    }
}
